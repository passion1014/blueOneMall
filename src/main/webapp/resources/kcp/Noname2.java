package com.blueone.board.domain;

public class NoticeInfo {
	private int ntIdx;
	private String ntType; 
	private String ntRegAdmin; 
	private String ntTitle; 
	private String ntContent; 
	private String ntRegDate;
	public int getNtIdx() {
		return ntIdx;
	}
	public void setNtIdx(int ntIdx) {
		this.ntIdx = ntIdx;
	}
	public String getNtType() {
		return ntType;
	}
	public void setNtType(String ntType) {
		this.ntType = ntType;
	}
	public String getNtRegAdmin() {
		return ntRegAdmin;
	}
	public void setNtRegAdmin(String ntRegAdmin) {
		this.ntRegAdmin = ntRegAdmin;
	}
	public String getNtTitle() {
		return ntTitle;
	}
	public void setNtTitle(String ntTitle) {
		this.ntTitle = ntTitle;
	}
	public String getNtContent() {
		return ntContent;
	}
	public void setNtContent(String ntContent) {
		this.ntContent = ntContent;
	}
	public String getNtRegDate() {
		return ntRegDate;
	}
	public void setNtRegDate(String ntRegDate) {
		this.ntRegDate = ntRegDate;
	}
	
	
}
