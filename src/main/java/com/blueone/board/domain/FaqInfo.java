package com.blueone.board.domain;

public class FaqInfo {
	
	private int faqIdx;
	private String faqType;
	private String faqQes;
	private String faqAns;
	private String faqRegDate;
	
	
	public int getFaqIdx() {
		return faqIdx;
	}
	public void setFaqIdx(int faqIdx) {
		this.faqIdx = faqIdx;
	}
	public String getFaqType() {
		return faqType;
	}
	public void setFaqType(String faqType) {
		this.faqType = faqType;
	}
	public String getFaqQes() {
		return faqQes;
	}
	public void setFaqQes(String faqQes) {
		this.faqQes = faqQes;
	}
	public String getFaqAns() {
		return faqAns;
	}
	public void setFaqAns(String faqAns) {
		this.faqAns = faqAns;
	}
	public String getFaqRegDate() {
		return faqRegDate;
	}
	public void setFaqRegDate(String faqRegDate) {
		this.faqRegDate = faqRegDate;
	}
	
	

}
