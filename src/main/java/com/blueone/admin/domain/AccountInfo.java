package com.blueone.admin.domain;

public class AccountInfo {

	private int 	accIdx;          
	private String  accShow;             	
	private int 	accGroup;  	
	private String  accBank;     	
	private int 	accNum;      
	private String  accName;      
	
	private String  bankType;   	     	
	private String 	bankName;
	
	
	
	public int getAccIdx() {
		return accIdx;
	}
	public void setAccIdx(int accIdx) {
		this.accIdx = accIdx;
	}
	public String getAccShow() {
		return accShow;
	}
	public void setAccShow(String accShow) {
		this.accShow = accShow;
	}
	public int getAccGroup() {
		return accGroup;
	}
	public void setAccGroup(int accGroup) {
		this.accGroup = accGroup;
	}
	public String getAccBank() {
		return accBank;
	}
	public void setAccBank(String accBank) {
		this.accBank = accBank;
	}
	public int getAccNum() {
		return accNum;
	}
	public void setAccNum(int accNum) {
		this.accNum = accNum;
	}
	public String getAccName() {
		return accName;
	}
	public void setAccName(String accName) {
		this.accName = accName;
	}
	public String getBankType() {
		return bankType;
	}
	public void setBankType(String bankType) {
		this.bankType = bankType;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	
	
	
}
